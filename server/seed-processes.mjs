import { createConnection } from "mysql2/promise";
import * as dotenv from "dotenv";

dotenv.config();

const DATABASE_URL = process.env.DATABASE_URL;
if (!DATABASE_URL) throw new Error("DATABASE_URL not set");

const conn = await createConnection(DATABASE_URL);

async function upsertProcess(p) {
  const [existing] = await conn.execute("SELECT id FROM processes WHERE name = ?", [p.name]);
  if (existing.length > 0) {
    await conn.execute(
      `UPDATE processes SET category=?, owner=?, backupOwner=?, documentationPct=?, status=?, automationOpportunity=?, description=?, steps=?, domainTag=?, linkedSessionIds=? WHERE name=?`,
      [p.category, p.owner, p.backupOwner, p.documentationPct, p.status, p.automationOpportunity, p.description, JSON.stringify(p.steps), p.domainTag, JSON.stringify(p.linkedSessionIds), p.name]
    );
    console.log(`  [updated] ${p.name}`);
  } else {
    await conn.execute(
      `INSERT INTO processes (name, category, owner, backupOwner, documentationPct, status, automationOpportunity, description, steps, domainTag, linkedSessionIds) VALUES (?,?,?,?,?,?,?,?,?,?,?)`,
      [p.name, p.category, p.owner, p.backupOwner, p.documentationPct, p.status, p.automationOpportunity, p.description, JSON.stringify(p.steps), p.domainTag, JSON.stringify(p.linkedSessionIds)]
    );
    console.log(`  [inserted] ${p.name}`);
  }
}

